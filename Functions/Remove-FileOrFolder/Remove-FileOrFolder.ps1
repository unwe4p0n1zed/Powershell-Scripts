function Remove-FileOrFolder {
    param(
        [CmdletBinding()]
        [Parameter(Mandatory = $true)]
        [Alias("ip")]
        [string]$itemPath,
        
        [Parameter(Mandatory = $true)]
        [Alias("it")]
        [ValidateSet("File", "Folder")]
        [string]$itemType,

        [Parameter(Mandatory = $false)]
        [Alias("ibd")]
        [bool]$includeBaseDirectory = $false
    )

    # Include base user directory
    $userDir = Get-Variable HOME -valueOnly

    if ($includeBaseDirectory) {
        $itemPath = Join-Path $userDir $itemPath
    }
    
    # Check if the item exists
    if (-not (Test-Path $itemPath)) {
        Write-Error "$itemType path '$itemPath' does not exist."
        return
    }

    # Check if item is file or folder
    if ($itemType -eq "File") {
        Remove-Item -Path $itemPath -Force
    } elseif ($itemType -eq "Folder") {
        Remove-Item -Path $itemPath -Force -Recurse
    }
    
    Write-Output "$itemType removed successfully from '$itemPath'"
}